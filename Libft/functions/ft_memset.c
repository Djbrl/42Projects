#include <stdlib.h>

void *ft_memset(void *str, int c, size_t n)
{
	int i;

	i = 0;
	char *ret = str;
	while (i < n)
	{
		ret[i] = (unsigned char)c;
		i++;
	}
	return ((void*)str);
}
