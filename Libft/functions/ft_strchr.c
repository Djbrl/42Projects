#include <stdlib.h>

char	*ft_strchr(char *str, int n)
{
	int		i;

	i = 0;
	if (n == 0)
		return ("\0");
	while (str[i])
	{
		if (str[i] == n)
			return (&str[i]);
		i++;
	}
	return ((char*)NULL);
}
