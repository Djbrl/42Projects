#include "libft.h"

char	*ft_strchr(const char *str, int n)
{
	int		i;

	i = 0;
	if (n == 0)
		return ("\0");
	while (str[i])
	{
		if (str[i] == n)
			return ((char *)str + i);
		i++;
	}
	return (0);
}
