#include "../../includes/cub3d.h"

int check_map_extension(char *path)
{
	int i;

	i = 0;
	while (path[i] != '.')
		i++;
	if (path[i + 1] == 'c' && path[i + 2] == 'u'
		&& path[i + 3] == 'b' && path[i + 4] == 0)
		return (1);
	return (0);
}
